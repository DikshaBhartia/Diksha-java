package com.company;

public class Pattern_Q2 {
    public static void main(String[] args) {
        int noOfRows = 5;
        for (int x = 0; x < noOfRows; x++ )
            {
            System.out.print("  ");
            }
            for (int y=0; y<=noOfRows; y++) {
                System.out.print("* ");
            }

            System.out.println();
    }
}
